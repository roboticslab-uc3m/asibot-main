double CartesianBot::toDeg(const double inRad) {
    return (inRad * 180.0 / 3.14159265);
}

// -----------------------------------------------------------------------------

double CartesianBot::toRad(const double inDeg) {
    return (inDeg * 3.14159265 / 180.0);
}

